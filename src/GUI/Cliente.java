    public void Listar(){
        try{
            ClienteDao dao = new ClienteDao();
            List<Clientes> listarClientes = dao.listarClientes();
            DefaultTableModel modelo = (DefaultTableModel) tbcliente.getModel();
            modelo.setNumRows(0);
            
            for(Clientes lc : listarClientes){
                modelo.addRow(new Object[]{
                  lc.getId(),
                  lc.getNome(),
                  lc.getApelido(),
                  lc.getTelefone(),
                  lc.getCelular(),
                  lc.getCep(),
                  lc.getCategoria(),
                  lc.getDesconto()
                });
            }
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Erro ao listar o Produto!");
        }
    }
    
    
    private void bteditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditarActionPerformed
        try {
            
        Clientes obj = new Clientes();
        
        obj.setNome(txtnome.getText());
        obj.setApelido(txtapelido.getText());
        obj.setTelefone(txttelefone.getText());
        obj.setCelular(txtcelular.getText());
        obj.setCep(txtcep.getText());
        obj.setCategoria(combo.getSelectedItem().toString());
        if(combo.getSelectedItem().equals("A")){
                obj.setDesconto("5%");
            }else if(combo.getSelectedItem().equals("B")){
                obj.setDesconto("15%");
            }else if(combo.getSelectedItem().equals("C")){
                obj.setDesconto("25%");
            }
        
        obj.setId(Integer.parseInt(txtid.getText()));
        
        dao = new ClienteDao();
        dao.alterarCliente(obj);
        
            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso");
        
        } catch (Exception e) {
            
              JOptionPane.showMessageDialog(null, "Erro ao alterar o cliente");
        }     
    }//GEN-LAST:event_bteditarActionPerformed

